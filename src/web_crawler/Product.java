package web_crawler;

public class Product {
	private String title;
	private int price;
	

	public String getTitle()
	{
		return  this.title;
	}
	public void setTitle(String title)
	{
		this.title=title;
	}
	
	public int getPrice()
	{
		return  this.price;
	}
	public void setPrice(int price)
	{
		this.price=price;
	}
	
	
}
